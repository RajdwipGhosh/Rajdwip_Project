// ***************** Switch Case in JS ***********************
//============================================

//Mon - 7am
//Tue - Thrus - 4am
//Fri - 9am
//Sat-Sun-8amm
let day = "Monday"

switch(day){
      case 'Monday' :
            console.log("7am");
            break;                  // Jump out of This Block terminate the present block
      case 'Tuesday' :
            console.log("4am");
            break;
      case 'Wednesday' :
            console.log("4am");
            break;    
      case 'Friday' :
            console.log("9am") 
            break
      case 'Saturday' :
            console.log("8am")  
            break
      case 'Sunday' :
            console.log("8am")  
      default :
             console.log("Vagggg");


}
console.log("Chal BSDK.....")


